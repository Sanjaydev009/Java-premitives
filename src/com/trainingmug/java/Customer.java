package com.trainingmug.java;

public class Customer {

    public static void main(String[] args) {
        /*
        Working with primitives
         */
        byte age;
        age = 23;
        System.out.println("Age :" + age);

        short orderValue;
        orderValue = 3000;
        System.out.println("Order Value :" + orderValue);

        int annualSalary;
        annualSalary = 1000000;
        System.out.println("Annual Salary :" + annualSalary);

        long totalSavings;
        totalSavings = 2345678900L;
        System.out.println("Total Savings :" + totalSavings);

        float   avgSalary;
        avgSalary = 54321.31f;
        System.out.println("Average Salary :" + avgSalary);

        double averageAnnualRevenue;
        averageAnnualRevenue = 543211233.31;
        System.out.println("Average Annual Revenue :" + averageAnnualRevenue);

        char gender;
        gender = 'M';
        System.out.println("Gender :" + gender);

        boolean isEligible;
        isEligible = true;
        System.out.println("Eligible :" + isEligible);



    }
}
